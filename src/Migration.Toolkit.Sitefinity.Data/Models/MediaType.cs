/// <summary>
/// Enumeration of supported media types for migration.
/// </summary>
public enum MediaType
{
    /// <summary>
    /// Image media type (jpg, png, gif, etc.)
    /// </summary>
    Image,
    /// <summary>
    /// Download media type (pdf, doc, txt, etc.)
    /// </summary>
    Download,
    /// <summary>
    /// Video media type (mp4, avi, mov, etc.)
    /// </summary>
    Video
}
